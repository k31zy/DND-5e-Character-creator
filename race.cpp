const std::vector<Race>& RaceData::all() {
    static std::vector<Race> races = load_races();
    return races;
}

const Race* RaceData::find(const std::string& id) {
    for (auto& r : all()) {
        if (r.id == id) return &r;
    }
    return nullptr;
}

void RaceData::list_all() {
    std::cout << "\n";
    std::cout << "  " << BOX_TL << hr(62) << BOX_TR << "\n";
    std::cout << "  " << BOX_V << " Доступные расы (PHB 2014)" << std::string(33, ' ') << BOX_V << "\n";
    std::cout << "  " << BOX_V << hr(62, " ") << BOX_V << "\n";
    std::cout << "  " << BOX_V << " " << pad_right("№", 4) << pad_right("Раса", 21) << pad_right("Скор.", 7) << "Бонусы характеристик    " << BOX_V << "\n";
    std::cout << "  " << BOX_V << hr(62) << BOX_V << "\n";
    int n = 1;
    for (auto& r : all()) {
        std::ostringstream speed_str;
        speed_str << r.speed << " фт";

        std::ostringstream bonuses;
        bool first = true;
        for (auto& [a, v] : r.ability_bonuses) {
            if (!first) bonuses << ", ";
            bonuses << "+" << v << " " << ABILITY_NAMES[static_cast<int>(a)];
            first = false;
        }

        std::cout << "  " << BOX_V << " " << pad_right(std::to_string(n++) + ".", 4)
                  << pad_right(r.name_ru, 21)
                  << pad_right(speed_str.str(), 7)
                  << pad_right(bonuses.str(), 26) << BOX_V << "\n";
    }
    std::cout << "  " << BOX_BL << hr(62) << BOX_BR << "\n\n";
}
