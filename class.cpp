const std::vector<DnDClass>& ClassData::all() {
    static std::vector<DnDClass> classes = load_classes();
    return classes;
}

const DnDClass* ClassData::find(const std::string& id) {
    auto& cs = all();
    for (auto& c : cs) {
        if (c.id == id) return &c;
    }
    return nullptr;
}

void ClassData::list_all() {
    std::cout << "\n";
    std::cout << "  " << BOX_TL << hr(62) << BOX_TR << "\n";
    std::cout << "  " << BOX_V << " Доступные классы (PHB 2014)" << std::string(30, ' ') << BOX_V << "\n";
    std::cout << "  " << BOX_V << hr(62, " ") << BOX_V << "\n";
    std::cout << "  " << BOX_V << " " << pad_right("№", 4)
              << pad_right("Класс", 16)
              << pad_right("Кость хит.", 12)
              << pad_right("Хар-ка", 18)
              << "Магия     " << BOX_V << "\n";
    std::cout << "  " << BOX_V << hr(62) << BOX_V << "\n";

    int n = 1;
    for (auto& c : all()) {
        std::string hit_die = "к" + std::to_string(c.hit_die);
        const char* ability = ABILITY_FULL[static_cast<int>(c.primary_ability)];
        const char* magic;
        switch (c.caster_type) {
            case CasterType::FULL:  magic = "Полный"; break;
            case CasterType::HALF:  magic = "Половина"; break;
            case CasterType::THIRD: magic = "Треть"; break;
            default: magic = "Нет";
        }

        std::cout << "  " << BOX_V << " " << pad_right(std::to_string(n++) + ".", 4)
                  << pad_right(c.name_ru, 16)
                  << pad_right(hit_die, 12)
                  << pad_right(ability, 18)
                  << pad_right(magic, 8) << BOX_V << "\n";
    }
    std::cout << "  " << BOX_BL << hr(62) << BOX_BR << "\n\n";
}

std::vector<ClassFeature> DnDClass::features_at_level(int level) const {
    std::vector<ClassFeature> result;
    for (auto& f : features) {
        if (f.level == level) result.push_back(f);
    }
    return result;
}

SpellSlots DnDClass::spell_slots(CasterType type, int level) {
    if (type == CasterType::NONE || level < 1) return {};

    int cl;
    switch (type) {
        case CasterType::FULL:  cl = level; break;
        case CasterType::HALF:  cl = level / 2; break;
        case CasterType::THIRD: cl = level / 3; break;
        default: return {};
    }

    SpellSlots s;

    if (cl >= 1) s.cantrips = (cl < 4) ? 2 : (cl < 10) ? 3 : 4;

    if (cl >= 1) { s.level1 = (cl < 2) ? 2 : (cl < 3) ? 3 : 4; }
    if (cl >= 3) { s.level2 = (cl < 4) ? 2 : 3; }
    if (cl >= 5) { s.level3 = (cl < 6) ? 2 : 3; }
    if (cl >= 7) { s.level4 = (cl < 8) ? 1 : (cl < 9) ? 2 : 3; }
    if (cl >= 9)  { s.level5 = (cl < 10) ? 1 : (cl < 18) ? 2 : 3; }
    if (cl >= 11) { s.level6 = (cl < 19) ? 1 : 2; }
    if (cl >= 13) { s.level7 = (cl < 20) ? 1 : 2; }
    if (cl >= 15) { s.level8 = 1; }
    if (cl >= 17) { s.level9 = 1; }

    return s;
}
