const std::vector<Background>& BackgroundData::all() {
    static std::vector<Background> bgs = load_backgrounds();
    return bgs;
}

const Background* BackgroundData::find(const std::string& id) {
    for (auto& b : all()) if (b.id == id) return &b;
    return nullptr;
}

void BackgroundData::list_all() {
    std::cout << "\n";
    std::cout << "  " << BOX_TL << hr(40) << BOX_TR << "\n";
    std::cout << "  " << BOX_V << " Доступные предыстории" << std::string(16, ' ') << BOX_V << "\n";
    std::cout << "  " << BOX_V << hr(40) << BOX_V << "\n";
    int n = 1;
    for (auto& b : all()) {
        std::string label = std::to_string(n++) + ". " + b.name_ru;
        std::cout << "  " << BOX_V << " " << pad_right(label, 37) << BOX_V << "\n";
    }
    std::cout << "  " << BOX_BL << hr(40) << BOX_BR << "\n\n";
}
